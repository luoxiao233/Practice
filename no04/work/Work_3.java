class Work_3{
	public static void main(String[] args) {
		/*答：为了提高代码的复用性，用来完成特定的代码块。
		格式：修饰词  返回值  方法名（参数类型 参数名，参数类型，参数名）｛
							方法体语句
		                  return 返回值
		｝
		修饰词：指的是例如public static 公共的静态的
		返回值：指的是返回的数据类型  例：int型就是返回int的数据
		方法名：符合规则
		参数类型：指的是方法体语句中运用的参数类型
		参数名：即为变量名
		方法体语句：包含参数名的代码块
		return: 结束方法
		返回值：方法结束后 返回一个东西给调用者



		*/

		System.out.println("Hello World!");
	}
}
